                    2023, generating a high amount of interest and interaction in a short amount of time.
                    In 2023, ISOL 24/7 HELP TEAM approached OSED to ask if the ISOL 24/7 HELP TEAM, who are experienced
                    in seafarers welfare and emotional support, could absorb the platform under our existing umbrella of
                    services.
